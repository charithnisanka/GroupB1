		<div class="col-md-6">
			<h3>Photogrammetric Scanning and printing</h3>
				<p>This encompasses scanning of aerial film; digital image compression services;
				and production of a wide variety of aerial image products such as photo mosaics, 
				photo index layouts, and contact prints that are reproduced on photo paper. Archive 
				quality, mylar, mounting and lamination options are also available.</p>
 
				<p>Our digital files are delivered either on CD/DVD/portable hard drives or via 
				online FTP transfer.</p>
	
				<p>For conventional film based projects, GeoTech performs direct scanning of the aerial
				negatives in preparation for digital aerotriangulation, map compilation, and image
				rectification. We use the most sophisticated precision photogrammetric roll fed 
				scanner (DSW-700) capable of resolutions of 12microns (2117 dpi) with 0.5micron accuracy.
				Our high quality digital imagery is stored in a secure environment on a 30-terrabyte
				RAID Level 5 data storage system.</p>
		</div> 
	</div> 
	
	<div class="row">

		<div class="col-md-6">
			<h3>Airborne Global Positioning System(AGPS)</h3>
					<p>GeoTech offers airborne GPS services that allow us to establish 
					photogrammetric control from the air as mapping photography is acquired. 
					We do this by recording a GPS coordinate for the precise geometric 
					center of the aerial photograph at the instant the camera shutter is 
					triggered. AGPS is appropriate for large mapping projects having 
					positional accuracy requirements for 2', 4' or 5' contours, and can 
					offer significant cost savings due to decreased field survey ground control.</p>
			
 
		</div> 
		<div class="col-md-6">
			<h3>Digital Aerial Triangulation</h3>
				<p>DAAT is a photogrammetric process to densify a framework of field-established ground 
				control to enable mapping over an entire project. It dramatically reduces the need for 
				survey time by eliminating the need to fully control all stereo models in the field. 
				This tried and tested technology is especially beneficial in large projects, and where 
				terrain or manmade features make access to and travel around a project site very 
				difficult. Analytical aerotriangulation also provides an intermediate quality control 
				check on all ground control, enabling any errors to be caught and corrected prior to 
				the mapping phase of the project. Our state-of-the-art facility provides all the 
				necessary equipment and personnel to perform analytical aerotriangulation through 
				conventional means, as well as through the latest softcopy automated techniques. </p>
		</div> 
	</div> 
	
	<div class="row">

		<div class="col-md-6">
			<h3>Light Detection and Ranging (LiDar)</h3>
				<p>With the exciting purchase of RIEGL's LMS-Q780 full waveform airborne laser scanner, 
				GeoTech now offers full LiDAR services including data aquisition, post-processing, 
				filtering, and classification.</p>
 
				<p>Light Detection and Ranging is an airborne-based sensing technology that provides 
				geo-referenced, three dimensional point data of the earth's surface. LIDAR integrates 
				three technological components: LASER ( Light Amplification by Simulated Emission of 
				Radiation) , GPS (Global Positioning System) and IMU (Inertial Management Unit) into a 
				single system.</p>
 
				<p>LiDAR offers the benefits of collecting vast amounts of elevation data within a short
				period of time, day or night and regardless of the sun's angle. It is complementary to
				(but not a replacement for) the traditional aerial mapping (photogrammetry) methodology.</p>
		</div> 
