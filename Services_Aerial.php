			<p>GeoTech utilizes next generation digital softcopy workstations, linked to high-speed stereo 
			graphics, enabling us to take advantage of high resolution 3-D digital sensor imagery to 
			perform precise measurements and data collection. These photogrammetric techniques enable us
			to capture planimetric features, topographic data, digital terrain models and contours that 
			are customized to our clients' requirements.</p>
	
			<p>Our sophisticated mapping software provides an unprecedented degree of control over the
			cartographic data that we can offer our clients. It also allows us to manipulate large
			quantities of information efficiently, formatting it precisely to client specifications,
			while running continual data integrity checks to ensure product quality.</p>
