			<h3>Aerial Imagery</h3>
			

				<p>GeoTech provides precision flight mission planning, 
					flying and acquisition of aerial imagery to engineering firms, 
					municipalities, government agencies, architects, surveyors and 
					planners for both government and private sectors.</p>
				 
				<p>Our aerial photography is acquired primarily using precision 
				Digital Aerial Mapping Cameras or the traditional film-based 
				aerial mapping cameras (such as the Leica Geosystems RC-30 or 
				Zeiss RMK Top 15) which are equipped with forward image-motion 
				compensation (FMC), GPS triggering and control capability, 
				and distortion-free lens systems. We offer a full digital workflow option, 
				from 3D flight planning all the way through to final delivery, 
				eliminating film processing and scanning completely; in fact, 
				this allows us to start working with aerial imagery that's hot off the runway.</p>
